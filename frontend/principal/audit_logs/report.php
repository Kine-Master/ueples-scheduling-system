<?php
require '../../../backend/config/db.php';
require '../../../backend/config/functions.php';
requireRole('principal');

// 1. CAPTURE FILTERS
$search = $_GET['search'] ?? '';
$action_filter = $_GET['action'] ?? '';
$start_date = $_GET['start_date'] ?? '';
$end_date = $_GET['end_date'] ?? '';

// 2. BUILD QUERY (LIMIT 100)
$sql = "SELECT a.*, u.first_name, u.last_name, u.role_id 
        FROM audit_log a
        LEFT JOIN user u ON a.user_id = u.user_id
        WHERE 1=1";

$params = [];

if (!empty($search)) {
    $sql .= " AND (u.first_name LIKE ? OR u.last_name LIKE ? OR a.user_action LIKE ?)";
    $term = "%$search%";
    array_push($params, $term, $term, $term);
}
if (!empty($action_filter)) {
    $sql .= " AND a.user_action = ?";
    $params[] = $action_filter;
}
if (!empty($start_date)) {
    $sql .= " AND a.timestamp >= ?";
    $params[] = "$start_date 00:00:00";
}
if (!empty($end_date)) {
    $sql .= " AND a.timestamp <= ?";
    $params[] = "$end_date 23:59:59";
}

$sql .= " ORDER BY a.timestamp DESC LIMIT 100"; // <--- LIMIT REQUIREMENT

$stmt = $pdo->prepare($sql);
$stmt->execute($params);
$logs = $stmt->fetchAll(PDO::FETCH_ASSOC);

