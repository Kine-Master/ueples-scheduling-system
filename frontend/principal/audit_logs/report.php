    <div class="no-print" style="margin-bottom:20px; text-align:right;">
        <button onclick="window.print()">Print Report</button>
        <button onclick="window.close()">Close</button>
    </div>

    <h2>System Audit Log Report</h2>
    <p>Generated on: <?= date('F d, Y h:i A') ?></p>
    <p>Filters: 
        <?= $action_filter ? "Action: $action_filter | " : "" ?>
        <?= $search ? "Search: $search | " : "" ?>
        Displaying last 100 matching records.
    </p>

    <table>
        <thead>
            <tr>
                <th>Time</th>
                <th>User</th>
                <th>Role</th>
                <th>Action</th>
                <th>IP Address</th>
            </tr>
        </thead>
        <tbody>
            <?php foreach($logs as $row): 
                $role = ($row['role_id'] == 1) ? 'Principal' : (($row['role_id'] == 2) ? 'Secretary' : 'Teacher');
            ?>
            <tr>
                <td><?= date('Y-m-d H:i:s', strtotime($row['timestamp'])) ?></td>
                <td><?= htmlspecialchars($row['last_name'] . ', ' . $row['first_name']) ?></td>
                <td><?= $role ?></td>
                <td><?= htmlspecialchars($row['user_action']) ?></td>
                <td><?= htmlspecialchars($row['ip_address']) ?></td>
            </tr>
            <?php endforeach; ?>
        </tbody>
